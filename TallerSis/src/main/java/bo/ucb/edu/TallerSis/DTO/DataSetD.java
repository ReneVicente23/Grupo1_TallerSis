package bo.ucb.edu.TallerSis.DTO;

public class DataSetD {
    private String x;
    private Double y;

    public DataSetD(String x, Double y) {
        this.x = x;
        this.y = y;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }
}
